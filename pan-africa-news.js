function initIntel(){

 const grid=document.getElementById('intelGrid');
 grid.innerHTML='';

 for(const [region,countries] of Object.entries(africaData)){

   let html=`
   <div class='region-block'>
   <div class='region-title'>
   ${region.replace(/([A-Z])/g,' $1').trim()}
   </div>
   `;

   for(const country in countries){
      html+=`
      <button
       class='country-btn'
       onclick="openCountry('${region}','${country}')">
       » ${country.replace(/([A-Z])/g,' $1').trim()}
      </button>
      `;
   }

   html+='</div>';

   grid.innerHTML+=html;
 }

}



function openCountry(region,country){

 let sources=africaData[region][country];

 document.getElementById(
 'modalCountry'
 ).innerText=
 'IDENTIFIED: '+country.toUpperCase();

 let html='';

 sources.forEach(src=>{
   html+=`
   <a
    class='modal-link'
    href='${src[1]}'
    target='_blank'>
    ${src[0]}
   </a>
   `;
 });

 document.getElementById(
 'modalLinks'
 ).innerHTML=html;

 document.getElementById(
 'modalOverlay'
 ).style.display='block';

 document.getElementById(
 'newsModal'
 ).style.display='block';

}


function closeModal(){

 document.getElementById(
 'modalOverlay'
 ).style.display='none';

 document.getElementById(
 'newsModal'
 ).style.display='none';

}

